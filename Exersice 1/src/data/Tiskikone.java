package data;

public class Tiskikone {

    private int id; 
    private String merkki; 
    private String malli; 
    private String persuohjelma;



    
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getMerkki() {
        return merkki;
    }
    public void setMerkki(String merkki) {
        this.merkki = merkki;
    }
    public String getMalli() {
        return malli;
    }
    public void setMalli(String malli) {
        this.malli = malli;
    }
    public String getPersuohjelma() {
        return persuohjelma;
    }
    public void setPersuohjelma(String persuohjelma) {
        this.persuohjelma = persuohjelma;
    } 


    

}
